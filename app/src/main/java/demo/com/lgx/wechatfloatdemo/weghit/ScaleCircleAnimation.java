public class ScaleCircleAnimation {
   private int x;
    private int y;
    private  float radius;


    public ScaleCircleAnimation(int x,int y,float radius){
        this.x=x;
        this.y=y;
        this.radius=radius;
    }

    public int getX() {
        return x;
    }


    public int getY() {
        return y;
    }


    public float getRadius() {
        return radius;
    }
}
